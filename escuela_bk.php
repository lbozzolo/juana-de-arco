					<p><i>El ni&ntilde;o ha de ser recibido con respeto, educado con amor, puesto en la vida finalmente, en libertad.</i>
					<br>~Rudolf Steiner
					</p>
					<p>Juana de Arco recibe ni&ntilde;os de nivel inicial (jard&iacute;n) y primario. Nuestra tarea se desarrolla en jornada simple, de 8 a 12 horas para el jard&iacute;n y de 7.50 a 13 horas para el nivel primario. Los grados superiores de primaria cuentan con actividades en poshora o contraturno. 
					<p>En jard&iacute;n ni&ntilde;os de 3 a 6 a&ntilde;os comparten con sus maestras actividades anuales, semanales y diarias, marcando el ritmo de cada momento. Contamos a la fecha con tres salas de jard&iacute;n, de entre 16 y 20 ni&ntilde;os en cada una.</p> 
					<p>En primaria, desde el 1er al 7mo grado, los ni&ntilde;os desarrollan en distintos cursos, adem&aacute;s de las actividades acad&eacute;micas, clases de m&uacute;sica, idiomas alem&aacute;n e ingl&eacute;s, pl&aacute;stica, gimnasia, huerta, euritmia, nataci&oacute;n, labores, carpinter&iacute;a, teatro, campamentos y otras. En el a&ntilde;o 2008 egresa nuestro 3er grupo de ni&ntilde;os de 7mo grado.</p> 
					<p>A lo largo del a&ntilde;o realizamos tambi&eacute;n actividades para toda la familia como kermesses, ferias del libro, jornadas de juego y jornadas de trabajo comunitario. </p>

					<p><b>Al comienzo</b>

<br>El "Instituto Juana de Arco" surge como un impulso conjunto de un grupo de padres y maestros formados en la pedagog&iacute;a Waldorf. Como un encuentro entre la necesidad de este grupo de padres, que quer&iacute;a elegir para sus hijos una instituci&oacute;n educativa cuyo ideario respondiera a sus convicciones filos&oacute;ficas, &eacute;ticas o religiosas  y docentes que, habiendo conocido la pedagog&iacute;a Waldorf, profundizaron su formaci&oacute;n en los seminarios espec&iacute;ficos, e interpretaron la necesidad de que tambi&eacute;n en la Capital Federal existiera un colegio como los que ya ven&iacute;an funcionando desde hace m&aacute;s de cincuenta a&ntilde;os en la Provincia de Buenos Aires.</p>

 

<p>Para su mejor desarrollo, los maestros y padres fundadores constituyeron una Fundaci&oacute;n, entidad sin fines de lucro que garantizara el libre y desinteresado desarrollo de la instituci&oacute;n, como patrimonio del mundo y no de las personas que la constitu&iacute;an.
</p>
 

<p>En el a&ntilde;o 1995 la Fundaci&oacute;n Alanus Ab Insulis en formaci&oacute;n, recibi&oacute; como donaci&oacute;n  la casa de la calle Ar&aacute;oz 1027 y con  otros aportes materiales y el trabajo de los padres se realizaron las refacciones y acondicionamientos necesarios.
</p>
 

<p>As&iacute;, con un grupo de  12 infantes, comenz&oacute; la actividad pedag&oacute;gica,  del Jard&iacute;n &ldquo;Juana de Arco&rdquo;.
</p>
 

<p>El nombre del Jard&iacute;n fue elegido en referencia al modelo que representa Juana de Arco como prototipo de joven con ideales, tan necesario en esta &eacute;poca en que vivimos.
</p>
 

<p>Con el transcurso de los a&ntilde;os el jard&iacute;n fue creciendo. Muchos padres, luego de haber vivido la experiencia educativa de la pedagog&iacute;a Waldorf, al llegar el momento del ingreso a la primaria, buscaban continuarla en alguno de los colegios existentes en la provincia de Buenos Aires, traslad&aacute;ndose toda la familia.
</p>

<p><b>Crecer a primaria</b>

<br>As&iacute;, con el deseo de garantizar la continuidad y articulaci&oacute;n entre los niveles que demandaba nuestra comunidad educativa, comprendimos la necesidad de ampliar nuestra propuesta pedag&oacute;gica al nivel primario.
</p>
 

<p>Este impulso fue sostenido por todos los padres y muy especialmente por las familias de los ni&ntilde;os o ni&ntilde;as con necesidades especiales integrados a los grupos de jard&iacute;n, que vivenciaron los enormes beneficios que esta pedagog&iacute;a tiene para sus hijos.
</p>
 

<p>En Junio de 1999 se concret&oacute; ante la Direcci&oacute;n General de Educaci&oacute;n de Gesti&oacute;n Privada, la presentaci&oacute;n del pedido de incorporaci&oacute;n a la ense&ntilde;anza oficial del "Instituto Juana de Arco" y la solicitud para la apertura del Nivel Inicial y del Primer Ciclo de la EGB. Con fecha 07/09/1999, mediante Actuaci&oacute;n N&ordm; 6546/99 de la Direcci&oacute;n General de Educaci&oacute;n de Gesti&oacute;n Privada recibimos la autorizaci&oacute;n para realizar la matr&iacute;cula provisoria en los niveles solicitados.
</p>
 

<p>Este nuevo proyecto de crecimiento implic&oacute; la necesidad de ampliar la infraestructura edilicia. La casa original resultaba suficiente para una primera etapa, pero carec&iacute;a de amplitud de espacios descubiertos y no permit&iacute;a una ampliaci&oacute;n futura.
</p>
 

<p>El edificio original de nuestro Instituto es una casa antigua, que data de principios del siglo pasado, de las llamadas &ldquo;casa chorizo&rdquo;, con acceso por escalera y techos altos, en el n&uacute;mero 1027 de la calle Ar&aacute;oz.
</p>
 

<p>En Diciembre de 1999 se alquil&oacute; la casa lindera (N&deg; 1035), que posee el mismo estilo arquitect&oacute;nico y que se integr&oacute; al edificio original, demoliendo parte de las paredes medianeras.
</p>
 

<p>El edificio actual posee una superficie de aproximadamente 640 m2, sobre un terreno de 16 m de frente por 41 m de largo, construida en una sola planta. Fue reciclado, acondicionado y habilitado para el uso de Jard&iacute;n de Infantes y Escuela Primaria.
</p>
 

<p>En el reciclado y reestructuraci&oacute;n se ha intentado implementar normas org&aacute;nicas de construcci&oacute;n, tratando de mantener el estilo original y respetar criterios funcionales y est&eacute;ticos. Se han utilizado materiales nobles, durables, de f&aacute;cil higiene, adecuados a las condiciones clim&aacute;ticas y respetando al m&aacute;ximo las normas de seguridad.
</p>
 

<p>En el a&ntilde;o 2006 el "Instituto Juana de Arco" recibe la incorporaci&oacute;n definitiva a la ense&ntilde;anza oficial con la caracter&iacute;stica A-1415 . El mismo a&ntilde;o egresa el primer grupo de alumnos de 7mo grado que ha realizado su ciclo primario completo en nuestro Instituto. Actualmente dichos alumnos contin&uacute;an sus estudios en colegios secundarios de la Ciudad de Buenos Aires (como la Escuela Sup. De Comercio Carlos Pellegrini, la Escuela de Cer&aacute;mica D.E. 02, por ejemplo).
</p>
 

<p>Actualmente cuenta con tres salas integradas de Nivel Inicial y siete grados de Educaci&oacute;n Primaria. La matr&iacute;cula total de la escuela es de 150 ni&ntilde;os. Interesados y compenetrados con nuestra propuesta pedag&oacute;gica llegan cada ma&ntilde;ana alumnos de los m&aacute;s diversos puntos de la Ciudad de Buenos Aires, incluso del AMBA. 
</p>					


