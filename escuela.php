					<p><i>El ni&ntilde;o ha de ser recibido con respeto, educado con amor, puesto en la vida finalmente, en libertad.</i>
					<br>~Rudolf Steiner
					</p>
					<p>La Escuela Juana de Arco recibe ni&ntilde;os y ni&ntilde;as y j&oacute;venes de nivel inicial, primario y secundario.
En jard&iacute;n, nuestra tarea se desarrolla en jornada simple, de 8 a 13 horas. Ni&ntilde;os y ni&ntilde;as de 3 a 6 a&ntilde;os comparten con sus maestras actividades anuales, semanales, menusuales y diarias, marcando el ritmo de cada momento. Contamos a la fecha con cuatro salas de jard&iacute;n, de entre 16 y 20 ni&ntilde;os y ni&ntilde;as en cada una.
					<p>En primaria, los ni&ntilde;os y ni&ntilde;as desarrollan, adem&aacute;s de las actividades acad&eacute;micas, clases de m&uacute;sica, idioma ingl&eacute;s, pl&aacute;stica, gimnasia, euritmia, nataci&oacute;n, labores, carpinter&iacute;a, teatro, campamentos y otras. El horario es de 7.50 a 13 horas, con actividades en poshora o contraturno para los grados superiores. </p> 
					<p>El nivel secundario se desarrolla durante el turno tarde, de 13 a 18.10 horas con talleres y educaci&oacute;n f&iacute;sica en contraturno, por la ma&ntilde;ana. En 2019 completamos el ciclo desde que inauguramos este nivel y con mucha alegr&iacute;a celebramos el egreso del primer grupo de 5to a&ntilde;o.</p> 
					<p>A lo largo del a&ntilde;o realizamos tambi&eacute;n actividades para toda la familia como kermeses, ferias de artesanos, tardes de talleres, grupos de estudio, charlas, pe&ntilde;as, conciertos y jornadas de trabajo comunitario y de mantenimiento y embellecimiento de nuestra escuela. </p>

					<p><b>Al comienzo</b>

<br>El "Instituto Juana de Arco" surgi&oacute; como un impulso conjunto de un grupo de familias y docentes formados en la pedagog&iacute;a Waldorf como respuesta a la necesidad de que existiera una escuela Waldorf en la Ciudad Aut&oacute;noma de Buenos Aires.</p>

 

<p>El encuentro entre maestros y maestras y familias fundadoras se formaliz&oacute; en la constituci&oacute;n de una Fundaci&oacute;n, entidad sin fines de lucro que garantizara el libre y desinteresado desarrollo de la instituci&oacute;n, como patrimonio del mundo y no de las personas que la conformaban.
</p>
 

<p>En el a&ntilde;o 1995 la Fundaci&oacute;n Alanus Ab Insulis, en formaci&oacute;n, recibi&oacute; como donaci&oacute;n la casa de la calle Ar&aacute;oz 1025/27 y con otros aportes materiales y el trabajo de las familias se realizaron las refacciones y acondicionamientos necesarios.
</p>
 

<p>As&iacute;, con un grupo de 12 ni&ntilde;os y ni&ntilde;as, comenz&oacute; la actividad pedag&oacute;gica, del Jard&iacute;n "Juana de Arco". Este nombre fue elegido en referencia al modelo que representa Juana de Arco, una joven con ideales, tan necesarios en esta &eacute;poca en que vivimos.
</p>
 

<p>Con el transcurso de los a&ntilde;os el jard&iacute;n fue creciendo. Muchas familias, luego de haber vivido la experiencia educativa de la pedagog&iacute;a Waldorf, al llegar el momento del ingreso a la primaria, buscaban continuarla en alguno de los colegios existentes en la provincia de Buenos Aires, debiendo trasladarse toda la familia.
</p>

<p><b>Crecer a primaria</b>

<br>As&iacute;, con el deseo de garantizar la continuidad y articulaci&oacute;n entre los niveles que demandaba nuestra comunidad educativa, comprendimos la necesidad de ampliar nuestra propuesta pedag&oacute;gica al nivel primario.
</p>
 

<p> Este impulso fue sostenido por las familias que vivenciaron los enormes beneficios que esta pedagog&iacute;a tiene para sus hijas e hijos.
</p>
 

<p>En junio de 1999 se concret&oacute; ante la Direcci&oacute;n General de Educaci&oacute;n de Gesti&oacute;n Privada, la presentaci&oacute;n del pedido de incorporaci&oacute;n a la ense&ntilde;anza oficial del "Instituto Juana de Arco" y la solicitud para la apertura del Nivel Inicial y del Primer Ciclo de la EGB. </p>
 

<p>Este nuevo proyecto implic&oacute; la necesidad de ampliar la infraestructura edilicia ya que la casa original  carec&iacute;a de amplitud de espacios descubiertos y no permit&iacute;a una ampliaci&oacute;n futura. En diciembre de 1999 se alquil&oacute; la casa lindera.
</p>
 

<p>Ambas casas fueron recicladas, acondicionadas y habilitadas para el uso de Jard&iacute;n de Infantes y Escuela Primaria.
</p>
 

<p>Actualmente la Escuela cuenta con dos plantas, m&aacute;s un subsuelo donde funciona el S.U.M, y se han ampliado los espacios abiertos con patios y terrazas.
</p>
 

<p>En el a&ntilde;o 2006 el "Instituto Juana de Arco" recibi&oacute; la incorporaci&oacute;n definitiva a la ense&ntilde;anza oficial con la caracter&iacute;stica A-1415. El mismo a&ntilde;o egres&oacute; el primer grupo de alumnos de 7mo grado que ha realizado su ciclo primario completo en nuestra Escuela.
</p>
 

<p>Juana de Arco sigui&oacute; creciendo y en el a&ntilde;o 2015, por un nuevo impulso de familias y docentes, se abri&oacute; el nivel secundario.
</p>
 
	


